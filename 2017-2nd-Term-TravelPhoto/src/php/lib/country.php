function getAlphabetically() {
    global $coun;
    $nation_arr = array();
    for ($i=65; $i<91; $i++) {
        $nation_arr[chr($i)] = array();
    }
    foreach($coun as $key=>$val) {
        $first = substr($val, 0, 1);
        array_push($nation_arr[$first], $val);
    }
    $nation_arr = array_filter($nation_arr);
    return $nation_arr;
